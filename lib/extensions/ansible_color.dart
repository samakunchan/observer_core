/// Pour l'affichage des couleurs dans la console
/// <br>Liste des couleurs:
/// - black
/// - red
/// - green
/// - yellow
/// - blue
/// - magenta
/// - cyan
/// - gray
/// - white
/// - reset
///
class AnsiColor {
  static const String black = '[30m';
  static const String red = '[31m';
  static const String green = '[32m';
  static const String yellow = '[33m';
  static const String blue = '[34m';
  static const String magenta = '[35m';
  static const String cyan = '[36m';
  static const String gray = '[37m';
  static const String white = '[38m';
  static const String reset = '[0m';
}
